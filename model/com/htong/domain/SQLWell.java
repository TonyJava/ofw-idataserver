public class SQLWell {
	private int id;
	private String wellnum;
	private int bengjing;
	private float bengshen;
	private float hanshui;
	private float niandu;
	private String dtu;
	
	
	
	public String getDtu() {
		return dtu;
	}
	public void setDtu(String dtu) {
		this.dtu = dtu;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getWellnum() {
		return wellnum;
	}
	public void setWellnum(String wellnum) {
		this.wellnum = wellnum;
	}
	public int getBengjing() {
		return bengjing;
	}
	public void setBengjing(int bengjing) {
		this.bengjing = bengjing;
	}
	public float getBengshen() {
		return bengshen;
	}
	public void setBengshen(float bengshen) {
		this.bengshen = bengshen;
	}
	public float getHanshui() {
		return hanshui;
	}
	public void setHanshui(float hanshui) {
		this.hanshui = hanshui;
	}
	public float getNiandu() {
		return niandu;
	}
	public void setNiandu(float niandu) {
		this.niandu = niandu;
	}
	
	

}
